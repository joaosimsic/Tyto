use super::Generator;
use crate::frontend::ast::{BaseType, TytoProgram, TytoType};

pub struct TypeScriptGenerator;

fn to_ts_type(ty: &TytoType) -> String {
    match ty {
        TytoType::Base(BaseType::String) => "string".to_string(),
        TytoType::Base(BaseType::Int) | TytoType::Base(BaseType::Float) => "number".to_string(),
        TytoType::Base(BaseType::Bool) => "boolean".to_string(),
        TytoType::Array(inner) => format!("{}[]", to_ts_type(inner)),
        TytoType::Optional(inner) => format!("{} | undefined", to_ts_type(inner)),
    }
}

impl Generator for TypeScriptGenerator {
    fn extension(&self) -> &'static str {
        "tyto.ts"
    }

    fn generate(&self, program: &TytoProgram) -> String {
        let mut output = String::new();

        output.push_str("// Automatically generated by Tyto\n");
        output.push_str("// Do not edit this file manually.\n\n");

        let state_interfaces: Vec<_> = program
            .states
            .iter()
            .map(|s| format!("{}State", s.name))
            .collect();

        for state in &program.states {
            let interface_name = format!("{}State", state.name);
            let fields = state.all_fields(program);

            output.push_str(&format!("export interface {} {{\n", interface_name));
            output.push_str(&format!("   readonly type: '{}';\n", state.name));

            if !fields.is_empty() {
                output.push_str("   readonly data: {\n");
                for field in &fields {
                    output.push_str(&format!(
                        "      {}: {};\n",
                        field.name,
                        to_ts_type(&field.field_type)
                    ));
                }
                output.push_str("   };\n");
            }

            output.push_str("}\n\n");
        }

        output.push_str(&format!(
            "export type AppState =\n  | {};\n\n",
            state_interfaces.join("\n  | ")
        ));

        output.push_str("export class Tyto {\n");

        for state in &program.states {
            let source_fields = state.all_fields(program);

            for transition in &state.transitions {
                let Some(target_state) = program.find_state(&transition.target) else {
                    continue;
                };

                let target_fields = target_state.all_fields(program);

                let new_fields: Vec<_> = target_fields
                    .iter()
                    .filter(|tf| !source_fields.iter().any(|sf| sf.name == tf.name))
                    .collect();

                let payload_arg = if new_fields.is_empty() {
                    String::new()
                } else {
                    let arg_types: Vec<_> = new_fields
                        .iter()
                        .map(|f| format!("{}: {}", f.name, to_ts_type(&f.field_type)))
                        .collect();
                    format!(", nextData: {{ {} }}", arg_types.join("; "))
                };

                output.push_str(&format!(
                    "  static transition(state: {}State, event: '{}'{}): {}State;\n",
                    state.name, transition.event, payload_arg, target_state.name
                ));
            }
        }

        output.push_str(
            "  static transition(state: AppState, event: string, nextData?: unknown): AppState {\n",
        );
        output.push_str("    switch (state.type) {\n");

        for state in &program.states {
            if state.transitions.is_empty() {
                continue;
            }

            let source_fields = state.all_fields(program);

            output.push_str(&format!("      case '{}':\n", state.name));

            for transition in &state.transitions {
                let Some(target_state) = program.find_state(&transition.target) else {
                    continue;
                };

                let target_fields = target_state.all_fields(program);

                let new_fields: Vec<_> = target_fields
                    .iter()
                    .filter(|tf| !source_fields.iter().any(|sf| sf.name == tf.name))
                    .collect();

                let data_assign = if target_fields.is_empty() {
                    String::new()
                } else if source_fields.is_empty() {
                    format!(", data: nextData as {}State['data']", target_state.name)
                } else if new_fields.is_empty() {
                    format!(
                        ", data: state.data as unknown as {}State['data']",
                        target_state.name
                    )
                } else {
                    format!(
                        ", data: ({{ ...state.data, ...(nextData as object) }} as unknown) as {}State['data']",
                        target_state.name
                    )
                };

                output.push_str(&format!(
                    "        if (event === '{}') return {{ type: '{}'{} }};\n",
                    transition.event, target_state.name, data_assign
                ));
            }
            output.push_str("        break;\n");
        }

        output.push_str("    }\n");
        output.push_str(
            "    throw new Error(`Invalid transition: ${event} from state ${state.type}`);\n",
        );
        output.push_str("  }\n");
        output.push_str("}\n");

        output
    }
}
